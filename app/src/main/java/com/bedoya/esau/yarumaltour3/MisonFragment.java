/**
 * A simple {@link Fragment} subclass.
 */
public class MisonFragment extends Fragment {

    MapView mapView;
    GoogleMap mMap3;

    public MisonFragment() {
        // Required empty public constructor
    }


    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View vMison = inflater.inflate(R.layout.fragment_mison, container, false);

        mapView = (MapView) vMison.findViewById(R.id.mapMison);
        mapView.onCreate(savedInstanceState);
        mMap3 = mapView.getMap();
        mMap3.getUiSettings().setMyLocationButtonEnabled(true);
        mMap3.getUiSettings().setZoomControlsEnabled(true);
        if (ActivityCompat.checkSelfPermission(getContext(), android.Manifest.permission.ACCESS_FINE_LOCATION) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(getContext(), android.Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {
             return null;
        }
        mMap3.setMyLocationEnabled(true);
        MapsInitializer.initialize(this.getActivity());

        CameraUpdate cameraUpdate = CameraUpdateFactory.newLatLngZoom(new LatLng(6.963177151939504,-75.41649095000002), 15);
        mMap3.animateCamera(cameraUpdate);
        LatLng lmison= new LatLng(6.963177151939504,-75.41649095000002);
        mMap3.addMarker(new MarkerOptions().position(lmison).title("MI SON CAFÃ‰ BAR").snippet("Bebidad y juegos"));
        return vMison;
    }
    @Override
    public void onResume() {
        mapView.onResume();
        super.onResume();
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        mapView.onDestroy();
    }

    @Override
    public void onLowMemory() {
        super.onLowMemory();
        mapView.onLowMemory();
    }


}
